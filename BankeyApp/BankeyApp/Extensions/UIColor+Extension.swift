import UIKit

extension UIColor {
    static var gradientDarkGray: UIColor {
        return UIColor(red: 239.0 / 255.0, green: 241.0 / 255.0, blue: 241.0 / 255.0, alpha: 1)
    }
    static var gradientLightGray: UIColor {
        return UIColor(red: 201.0 / 255.0, green: 201.0 / 255.0, blue: 201.0 / 255.0, alpha: 1)
    }
}
